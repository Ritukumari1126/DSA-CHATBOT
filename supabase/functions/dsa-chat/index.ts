import { serve } from "https://deno.land/std@0.168.0/http/server.ts";

const corsHeaders = {
  'Access-Control-Allow-Origin': '*',
  'Access-Control-Allow-Headers': 'authorization, x-client-info, apikey, content-type, x-supabase-client-platform, x-supabase-client-platform-version, x-supabase-client-runtime, x-supabase-client-runtime-version',
};

serve(async (req) => {
  if (req.method === 'OPTIONS') {
    return new Response(null, { headers: corsHeaders });
  }

  try {
    const { messages } = await req.json();
    const LOVABLE_API_KEY = Deno.env.get("LOVABLE_API_KEY");
    if (!LOVABLE_API_KEY) throw new Error("LOVABLE_API_KEY is not configured");

    const response = await fetch("https://ai.gateway.lovable.dev/v1/chat/completions", {
      method: "POST",
      headers: {
        Authorization: `Bearer ${LOVABLE_API_KEY}`,
        "Content-Type": "application/json",
      },
      body: JSON.stringify({
        model: "google/gemini-3-flash-preview",
        messages: [
          {
            role: "system",
            content: `You are an expert Data Structure and Algorithm (DSA) Instructor. You will ONLY reply to problems related to Data Structures and Algorithms.

Your guidelines:
- Explain concepts in the simplest way possible with clear examples
- Use code examples in multiple languages when helpful (prefer C++, Java, Python)
- Format code blocks with proper syntax highlighting using markdown
- Break down complex algorithms step-by-step
- Include time and space complexity analysis when relevant
- Use diagrams with ASCII art when it helps visualization
- If a user asks something NOT related to DSA, reply politely: "I appreciate your question! However, I specialize in Data Structures and Algorithms. Please ask me something related to DSA â€” like arrays, linked lists, trees, graphs, sorting, dynamic programming, and more! ðŸš€"

Topics you cover: Arrays, Linked Lists, Stacks, Queues, Trees, Binary Trees, BST, AVL Trees, Heaps, Graphs, Hash Tables, Sorting Algorithms, Searching Algorithms, Dynamic Programming, Greedy Algorithms, Backtracking, Divide and Conquer, Bit Manipulation, String Algorithms, and Complexity Analysis.`,
          },
          ...messages,
        ],
        stream: true,
      }),
    });

    if (!response.ok) {
      if (response.status === 429) {
        return new Response(JSON.stringify({ error: "Rate limit exceeded. Please try again in a moment." }), {
          status: 429,
          headers: { ...corsHeaders, "Content-Type": "application/json" },
        });
      }
      if (response.status === 402) {
        return new Response(JSON.stringify({ error: "Usage limit reached. Please add credits." }), {
          status: 402,
          headers: { ...corsHeaders, "Content-Type": "application/json" },
        });
      }
      const t = await response.text();
      console.error("AI gateway error:", response.status, t);
      return new Response(JSON.stringify({ error: "AI service error" }), {
        status: 500,
        headers: { ...corsHeaders, "Content-Type": "application/json" },
      });
    }

    return new Response(response.body, {
      headers: { ...corsHeaders, "Content-Type": "text/event-stream" },
    });
  } catch (e) {
    console.error("dsa-chat error:", e);
    return new Response(JSON.stringify({ error: e instanceof Error ? e.message : "Unknown error" }), {
      status: 500,
      headers: { ...corsHeaders, "Content-Type": "application/json" },
    });
  }
});
